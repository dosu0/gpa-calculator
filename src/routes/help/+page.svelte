<h2>How to Add a class</h2>

<ol>
    <li>Click on text box that says "enter a subject..."</li>
    <li>Type the name of your subject</li>
    <li>Press enter</li>
</ol>

<h2>How to Change a Grade</h2>
<ul>
    <li>Look for the subject of the grade of which you plan to change</li>
    <li>
        On the right of that subject should be a white text box with a grade, simply click on it and
        input a new grade
    </li>
    <li>Alternatively, click on the up and down arrows</li>
</ul>
